// Extracting Header
var header = [];
for (var i = 0; i < jsonData.length; i++) {
    for (var key in jsonData[i]){
        if (header.indexOf(key) == -1) {
            header.push(key);
        }
    }            
}

// Creating table format
var table = document.createElement("table");
table.setAttribute("id", "myTable");

// Creating table contents
var tr = table.insertRow(-1);

for (var i = 0; i < header.length; i++){
    var th = document.createElement("th");
    var th_old = header[i].charAt(0).toUpperCase() + header[i].slice(1);
    th.innerHTML = th_old.replace(/_/g, " ");
    tr.appendChild(th);
}

// Filling data
for (var i = 0; i < jsonData.length; i++) {

    tr = table.insertRow(-1);

    for (var j = 0; j < header.length; j++) {
        var td = tr.insertCell(-1);
        
        if (j == 0){
            td.innerHTML = i+1;
        }else{
            td.innerHTML = jsonData[i][header[j]];
        }
    }
}

// Display data
var divContainer = document.getElementById("showData");
divContainer.appendChild(table);




function search() {
    var input = document.getElementById("myInput");
    var filter = input.value.toUpperCase();
    var table1 = document.getElementById("myTable");
    var tr1 = table1.getElementsByTagName("tr");
    

    for (i = 0; i < tr1.length; i++) {
        td = tr1[i].getElementsByTagName("td")[1];
        
        if (td) {
          txtValue = td.textContent || td.innerText;
          if (txtValue.toUpperCase().indexOf(filter) > -1) {
            tr1[i].style.display = "";
          } else {
            tr1[i].style.display = "none";
          }
        } 
      }
}